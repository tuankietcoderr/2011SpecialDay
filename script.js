function RenderCard() {
  const item = data[index];
  const { name, wishes: wish } = item;
  const fullName = document.getElementById("full-name");
  const wishes = document.getElementById("wishes");
  let dHeight = wishes.style.lineHeight;
  console.log(dHeight);
  const pHeight = wishes.offsetHeight / parseInt(dHeight);
  fullName.innerText = name;
  wishes.innerText = wish;
  wishes.style.height = pHeight + "px";
}

function CardAction() {
  const previous = document.getElementById("previous");
  const next = document.getElementById("next");

  previous.onclick = function () {
    index -= 1;
    console.log(1);
    if (index < 0) {
      index = data.length - 1;
    }
    RenderCard();
  };

  next.onclick = function () {
    index += 1;
    if (index > data.length - 1) {
      index = 0;
    }
    RenderCard();
  };
}

CardAction();
